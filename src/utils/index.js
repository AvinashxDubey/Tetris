export const random = (min, max) => {
  return Math.floor(Math.random() * (max - min + 1)) + min
}

export const gridDefault = () => {
  const rows = 18;
  const cols = 10;
  const grid = [];

  for(let i = 0; i < rows; i++){
    for(let j = 0; j < cols; j++){
      grid[i].push(0);
    }
  }

  return grid;
}

